    private void btRotationActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btRotationActionPerformed
    {//GEN-HEADEREND:event_btRotationActionPerformed
        int ang;
        do
        {
            ang = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe o Ã‚ngulo(0-360): "));
        } while (ang < 0 || ang > 360);
        points = t.rotation(ang, points);
        draw(points);
        
    }//GEN-LAST:event_btRotationActionPerformed

    private void btScaleActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btScaleActionPerformed
    {//GEN-HEADEREND:event_btScaleActionPerformed
        int x = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe X: "));
        int y = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe Y:"));
        points = t.scale(new Point(x, y), points);
        draw(points);
    }//GEN-LAST:event_btScaleActionPerformed

    private void btTranslationActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btTranslationActionPerformed
    {//GEN-HEADEREND:event_btTranslationActionPerformed
        int val = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe: "));
        Point pQtd = new Point(val, val);
        points = t.translation(pQtd, points);
        draw(points);
    }//GEN-LAST:event_btTranslationActionPerformed

       
